from django.test import TestCase
from rest_framework.test import APITestCase
from rest_framework.test import APIRequestFactory
from rest_framework import status
from django.urls import reverse
from django.contrib.auth.models import User
from rest_framework.authtoken.models import Token


class AccountsTestCase(APITestCase):
    def setUp(self):
        # We want to go ahead and originally create a user.
        self.test_user = User.objects.create_user(
            "test", "test@testertester.com", "blahblahblahblah"
        )

        # URL for creating an account.
        self.create_url = reverse("user-register")

    def test_create_user(self):
        data = {
            "username": "poochinski",
            "email": "poochinski@tester.com",
            "password": "somepassword123",
        }

        response = self.client.post(self.create_url, data, format="json")
        self.assertEqual(User.objects.count(), 2)
        self.assertEqual(response.status_code, status.HTTP_201_CREATED)
        self.assertEqual(response.data["username"], data["username"])
        self.assertEqual(response.data["email"], data["email"])
        self.assertFalse("password" in response.data)

    def test_create_user_with_too_long_username(self):
        data = {
            "username": "finfinfin" * 10,
            "email": "asdfg@gmail.com",
            "password": "foobar",
        }

        response = self.client.post(self.create_url, data, format="json")
        self.assertEqual(response.status_code, status.HTTP_400_BAD_REQUEST)
        self.assertEqual(User.objects.count(), 1)
        self.assertEqual(len(response.data["username"]), 1)

    def test_create_user_with_no_username(self):
        data = {"username": "", "email": "ntu@example.com", "password": "foobarbaz"}

        response = self.client.post(self.create_url, data, format="json")
        self.assertEqual(response.status_code, status.HTTP_400_BAD_REQUEST)
        self.assertEqual(User.objects.count(), 1)
        self.assertEqual(len(response.data["username"]), 1)

    def test_create_user_with_preexisting_username(self):
        data = {"username": "test", "email": "user@example.com", "password": "testuser"}

        response = self.client.post(self.create_url, data, format="json")
